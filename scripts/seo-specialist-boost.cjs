function buildFAQSchema(faqs) {
  return {
    "@context": "https://schema.org",
    "@type": "FAQPage",
    "mainEntity": faqs.map(f => ({
      "@type": "Question",
      "name": f.q,
      "acceptedAnswer": { "@type": "Answer", "text": f.a }
    }))
  };
}

let updated = 0;

for (const [slug, boost] of Object.entries(BOOSTS)) {
  const filePath = path.join(doctorsDir, `${slug}.html`);
  if (!fs.existsSync(filePath)) { console.error(`[SKIP] ${slug}`); continue; }

  let html = fs.readFileSync(filePath, 'utf8');

  // 1. TITLE
  html = html.replace(/<title>[^<]*<\/title>/, `<title>${boost.title}</title>`);

  // 2. META DESCRIPTION
  html = html.replace(/<meta name="description" content="[^"]*">/, `<meta name="description" content="${boost.description}">`);

  // 3. META ABSTRACT
  html = html.replace(/<meta name="abstract" content="[^"]*">/, `<meta name="abstract" content="${boost.description}">`);

  // 4. META AI-SUMMARY
  html = html.replace(/<meta name="ai-summary" content="[^"]*">/, `<meta name="ai-summary" content="${boost.schemaDesc}">`);

  // 5. META KEYWORDS
  html = html.replace(/<meta name="keywords" content="[^"]*">/, `<meta name="keywords" content="${boost.keywords}">`);

  // 6. OG TAGS
  html = html.replace(/<meta property="og:title" content="[^"]*">/, `<meta property="og:title" content="${boost.title}">`);
  html = html.replace(/<meta property="og:description" content="[^"]*">/, `<meta property="og:description" content="${boost.ogDescription}">`);

  // 7. TWITTER TAGS
  html = html.replace(/<meta name="twitter:title" content="[^"]*">/, `<meta name="twitter:title" content="${boost.title}">`);
  html = html.replace(/<meta name="twitter:description" content="[^"]*">/, `<meta name="twitter:description" content="${boost.ogDescription}">`);

  // 8. UPDATE SCHEMA DESCRIPTION in Person JSON-LD
  // Find the Person schema and update its description
  const personSchemaRegex = /("@type": "Person",[^}]*"description": ")[^"]*(")/;
  html = html.replace(personSchemaRegex, `$1${boost.schemaDesc}$2`);

  // 9. REPLACE FAQ SCHEMA
  // Remove old FAQ schema block and insert new one
  const faqRegex = /<script type="application\/ld\+json">\s*\{[^]*?"@type": "FAQPage"[^]*?\}<\/script>/;
  const newFaqSchema = `<script type="application/ld+json">\n${JSON.stringify(buildFAQSchema(boost.faqs), null, 2)}\n</script>`;
  
  if (faqRegex.test(html)) {
    html = html.replace(faqRegex, newFaqSchema);
  }

  fs.writeFileSync(filePath, html, 'utf8');

  // Verify
  const verify = fs.readFileSync(filePath, 'utf8');
  const titleOk = verify.includes(`<title>${boost.title}</title>`);
  const faqCount = (verify.match(/"@type": "Question"/g) || []).length;
  const kwCount = (boost.keywords.match(/,/g) || []).length + 1;

  console.log(`[${titleOk ? 'OK' : 'FAIL'}] ${slug}: title=${titleOk} | faq=${faqCount} | kw=${kwCount}`);
  if (titleOk) updated++;
}

