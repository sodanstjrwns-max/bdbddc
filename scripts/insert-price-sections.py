changed, skipped, missing = [], [], []
for path, data in PAGES.items():
    if not os.path.exists(path):
        missing.append(path); continue
    html = open(path, encoding="utf-8").read()
    if MARKER in html:
        skipped.append(path); continue
    anchor = '    <section class="cta-section">'
    if anchor not in html:
        # 다른 들여쓰기 변형 시도
        m = re.search(r'^([ \t]*)<section class="cta-section">', html, re.M)
        if not m:
            missing.append(path + " (cta-section 없음)"); continue
        anchor = m.group(0)
    rows = "\n".join(ROW_T.format(name=r[0], price=r[1], note=r[2]) for r in data["rows"])
    section = TEMPLATE.format(title=data["title"], lead=data["lead"], rows=rows, notice=data["notice"])
    html = html.replace(anchor, section + anchor, 1)
    open(path, "w", encoding="utf-8").write(html)
    changed.append(path)

print(f"✅ 삽입 완료: {len(changed)}개")
for p in changed: print("  +", p)
if skipped: print(f"⏭️ 이미 존재(스킵): {len(skipped)}개", skipped)
if missing: print(f"⚠️ 대상 없음: {missing}")
